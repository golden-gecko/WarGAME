#include "WarGAME.h"

#include "Sound.h"

//------------------------------------------------------------------------------

Sound::Sound(string name, FSLsound* fslSound)

	:	m_name(name),
		m_sound(fslSound)

{

}

//------------------------------------------------------------------------------

void Sound::play() {

	m_sound->Play();

}

//------------------------------------------------------------------------------

void Sound::setLooping(bool looping) {

	m_sound->SetLooping(looping);

}

//------------------------------------------------------------------------------

void Sound::setPosition(float x, float y, float z) {

	m_sound->SetPosition(x, y, z);

}

//------------------------------------------------------------------------------

void Sound::setPosition(const Vector3& position) {

	m_sound->SetPosition(position.x, position.y, position.z);

}

//------------------------------------------------------------------------------

void Sound::stop() {

	m_sound->Stop();

}

